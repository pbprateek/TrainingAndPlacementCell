package com.aikya.rnsit.trainingandplacementcell.Data;

public class Student {

    private int id;
    private String email;
    private String password;
    private String name;
    private String contact;
    private String branch;
    private String marks10;
    private String marks12;
    private String marksbtech;
    private String year10;
    private String year12;
    private String yearbtech;
    private int placed;

    public Student() {

    }

    public int getPlaced() {
        return placed;
    }

    public void setPlaced(int placed) {
        this.placed = placed;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getContact() {
        return contact;
    }

    public void setContact(String contact) {
        this.contact = contact;
    }

    public String getBranch() {
        return branch;
    }

    public void setBranch(String branch) {
        this.branch = branch;
    }

    public String getMarks10() {
        return marks10;
    }

    public void setMarks10(String marks10) {
        this.marks10 = marks10;
    }

    public String getMarks12() {
        return marks12;
    }

    public void setMarks12(String marks12) {
        this.marks12 = marks12;
    }

    public String getMarksbtech() {
        return marksbtech;
    }

    public void setMarksbtech(String marksbtech) {
        this.marksbtech = marksbtech;
    }

    public String getYear10() {
        return year10;
    }

    public void setYear10(String year10) {
        this.year10 = year10;
    }

    public String getYear12() {
        return year12;
    }

    public void setYear12(String year12) {
        this.year12 = year12;
    }

    public String getYearbtech() {
        return yearbtech;
    }

    public void setYearbtech(String yearbtech) {
        this.yearbtech = yearbtech;
    }
}
